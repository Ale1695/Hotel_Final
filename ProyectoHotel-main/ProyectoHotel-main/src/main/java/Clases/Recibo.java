package Clases;

import java.time.LocalDate;

/**
 *
 * @author sales
 */
public class Recibo {
    private String idReserva,cedula,observaciones;
    private LocalDate fecha;
    private double monto,impuesto,descuento;

    public Recibo() {
    }

    public Recibo(String idReserva, String cedula, String observaciones, LocalDate fecha, double monto, double impuesto, double descuento) {
        this.idReserva = idReserva;
        this.cedula = cedula;
        this.observaciones = observaciones;
        this.fecha = fecha;
        this.monto = monto;
        this.impuesto = impuesto;
        this.descuento = descuento;
    }

    public String getIdReserva() {
        return idReserva;
    }

    public void setIdReserva(String idReserva) {
        this.idReserva = idReserva;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }

    public LocalDate getFecha() {
        return fecha;
    }

    public void setFecha(LocalDate fecha) {
        this.fecha = fecha;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }

    public double getImpuesto() {
        return impuesto;
    }

    public void setImpuesto(double impuesto) {
        this.impuesto = impuesto;
    }

    public double getDescuento() {
        return descuento;
    }

    public void setDescuento(double descuento) {
        this.descuento = descuento;
    }
}
